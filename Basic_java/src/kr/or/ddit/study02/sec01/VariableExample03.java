package kr.or.ddit.study02.sec01;

public class VariableExample03 {
	public static void main(String[] args) {
		/*
		 변수 a에 5 값을 저장
		 변수 b에 7값을 저장
		 변수 result에 a+b 값을 저장해보기.
		 
		 */
		
		
		int a = 5;
		int b = 7;
		int result = a+b;
		
		System.out.println(a + "+" + b + "=" + result);
		System.out.println("a 값은 : " + a);
		
		a = 8;
		System.out.println("a 값은 : " + a);
	}

}
