package kr.or.ddit.study01;

public class ShortCut {
	
	public static void main(String[] args) {
		/*
		 * 단축기
		 * ctrl+shift+l : 모든 단축키 안내
		 * ctrl+space   : 자동완성
		 * ctrl+alt+위아래 방향키 : 코드 복사
		 * alt+방향키           : 코드 위치 바꾸기
		 * ctrl+shift+/ : 범위주석
		 * ctrl+shift+c : 한줄주석/취소
		 * ctrl+n       : 신규생성
		 * ctrl+d       : 한줄삭제
		 * 
		 * ctrl+z       : 실행취소
		 * ctrl+y       : 실행취소의 취소
		 * ctrl+s       : 저장
		 * ctrl+shift+s : 전체저장
		 * 
		 * ctrl+F11     : 소스실행
		 * ctrl+shift+f : 정렬
		 */
		
		System.out.println("3");
		System.out.println("2");
		System.out.println("1");
	}

}
